                <br><img src="../src/images/colico.png" alt="Colicos" width="120" height="120">
                <h3>Cólicos, síntomas y causas</h3>
                <p>Los gritos y el llanto son normales en los bebés, especialmente durante los tres primeros meses. Y el rango de lo que es un llanto normal es difícil de precisar. En general, el cólico se define como el llanto durante tres o más horas al día, tres o más días a la semana, durante tres o más semanas.</p>
                    <p>Las características del cólico pueden incluir lo siguiente:</p>
                    <ul>
                        <li>Llanto intenso que puede parecer más bien un grito o una expresión de dolor</li>
                        <li>Llanto sin razón aparente, a diferencia de llorar para expresar el hambre o la necesidad de un cambio de pañal</li>
                        <li>Alboroto extremo, incluso después de que el llanto haya disminuido</li>
                        <li>Tiempo predecible, con episodios que a menudo ocurren en la noche</li>
                        <li>Decoloración facial, como el enrojecimiento de la cara o la piel más pálida alrededor de la boca</li>
                        <li>Tensión corporal, como piernas estiradas o rígidas, brazos rígidos, puños apretados, espalda arqueada o abdomen tenso</li>
                    </ul>
                    <p>A veces se produce un alivio de los síntomas después de que el bebé expulse gases o tenga una evacuación intestinal. El gas es probablemente el resultado de la ingestión de aire durante el llanto prolongado.</p>
                    <p>Debes consultar al médico cuando el llanto es excesivo e inconsolable puede deberse a un cólico o ser una indicación de una enfermedad o afección que cause dolor o malestar. Programa una cita con el médico de tu hijo para que le haga un examen completo si tu bebé experimenta llanto excesivo u otros signos o síntomas de cólico.</p>
                    <p>Se desconoce la causa de los cólicos. Pueden ser provocados por muchos factores que contribuyen. Si bien se han explorado muchas causas, a los investigadores les resulta difícil explicar todas las características importantes; por ejemplo, por qué suelen comenzar a finales del primer mes de vida, cómo varían entre bebés, por qué se manifiestan en ciertos momentos del día y por qué se resuelven por sí solos con el tiempo.</p> <p>Estos son los posibles factores que contribuyen y que se han explorado:</p> <ul> <li>Aparato digestivo que no se ha desarrollado por completo</li> <li>Desequilibrio de bacterias saludables en el tracto digestivo</li> <li>Alergias alimentarias o intolerancias a alimentos</li> <li>Alimentación en exceso o insuficiente, o eructos poco frecuentes</li> <li>Forma temprana de migraña infantil</li> <li>Estrés o ansiedad familiar</li> </ul>
