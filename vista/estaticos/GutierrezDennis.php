            <h2>Diarrea</h2>
            <article>
                <br><img src="../src/images/diarrea.png" alt="Diarrea" width="120" height="120">
                <br><h3>Diarrea, síntomas y causas</h3>
                <p>Los signos y síntomas asociados con la diarrea (heces acuosas) pueden ser los siguientes:</p>
                    <ul>
                        <li>Cólicos abdominales o dolor</li>
                        <li>Hinchazón abdominal</li>
                        <li>Náuseas</li>
                        <li>Vómitos</li>
                        <li>Fiebre</li>
                        <li>Sangre en las heces</li>
                        <li>Mucosidad en las heces</li>
                        <li>Necesidad urgente de defecar</li>
                    </ul>
                    <p>Varias enfermedades y afecciones pueden causar diarrea, incluidas las siguientes:</p>
                    <ul>
                        <li><strong>Virus.</strong> Los virus que pueden causar diarrea son el virus de Norwalk (también conocido como norovirus), el adenovirus entérico, el astrovirus, el citomegalovirus y el virus de la hepatitis. El rotavirus es una causa frecuente de diarrea infantil aguda. El virus que causa la enfermedad por coronavirus 2019 (COVID-19) se ha asociado con síntomas gastrointestinales, que incluyen náuseas, vómitos, y diarrea.</li>
                        <li><strong>Bacterias y parásitos.</strong> La exposición a bacterias patogénicas, como la E. coli, o parásitos a través de alimentos o agua contaminados, causa diarrea. Cuando se viaja a países en desarrollo, la diarrea causada por bacterias y parásitos a menudo se denomina "diarrea del viajero". La Clostridioides difficile (también conocida como C. difficile) es otro tipo de bacteria que causa diarrea y puede aparecer después de un tratamiento con antibióticos o durante una hospitalización.</li>
                        <li><strong>Medicamentos.</strong> Muchos medicamentos, como los antibióticos, pueden causar diarrea. Los antibióticos alivian las infecciones mediante la eliminación de las bacterias malas, pero también eliminan las bacterias buenas. Esto altera el equilibrio natural de las bacterias en los intestinos, lo que causa diarrea o una infección superpuesta como C. difficile. Otros medicamentos que causan diarrea son los medicamentos contra el cáncer y los antiácidos con magnesio.</li>
                        <li><strong>Intolerancia a la lactosa.</strong> La lactosa es un azúcar que se encuentra en la leche y en otros productos lácteos. Las personas que tienen dificultad para digerir la lactosa tienen diarrea después de comer productos lácteos. La intolerancia a la lactosa puede aumentar con la edad debido a que los niveles de la enzima que ayuda a digerir la lactosa disminuyen a medida que envejeces.</li>
                        <li><strong>Fructosa.</strong> La fructosa es un azúcar que se encuentra naturalmente en las frutas y la miel. A veces se agrega como endulzante a ciertas bebidas. La fructosa puede causar diarrea en las personas que tienen problemas para digerirla.</li>
                        <li><strong>Edulcorantes.</strong> El sorbitol, el eritritol y el manitol (los edulcorantes son azúcares no absorbibles que se encuentran en la goma de mascar y otros productos sin azúcar), pueden causar diarrea en personas que de otra manera estarían sanas.</li>
                        <li><strong>Cirugía.</strong> Las cirugías para extirpar la vesícula biliar o una parte del intestino pueden causar diarrea.</li>
                        <li><strong>Otros trastornos digestivos.</strong> La diarrea crónica tiene algunas otras causas, como el síndrome de colon irritable, la enfermedad de Crohn, la colitis ulcerativa, la enfermedad celíaca, la colitis microscópica y el sobrecrecimiento bacteriano en el intestino delgado.</li>
                    </ul>
